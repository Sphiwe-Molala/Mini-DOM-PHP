		public function toString() {

			$text = "";

			foreach ($this as $member => $value) {

				if ( $this->{ $member } !== null ) {

					$text .= $this -> dashCamelCase( $member ) . ":" . preg_replace("/;$/", "", $value) . ";";

				}

			}

			return $text;

		}

		private function dashCamelCase( $camelCased ) {

			$tokens = preg_split("/[A-Z]+/", $camelCased);
			$chars = preg_replace("/[a-z]*/", "", $camelCased);

			if ( count( $tokens ) < 1 )

				return $camelCased;

			else {

				for ( $i = 0; $i < strlen( $chars ); $i++) { 
					
					$tokens[ $i + 1 ] = "-" . strtolower( substr($chars , $i, 1 ) ) . $tokens[ $i + 1 ];

				}

			}

			return implode( "", $tokens);

		}

		public function set__( $memberOrArray , $value = ""){

			if ( !is_array( $memberOrArray ) ) {

				$memberOrArray = array( $memberOrArray => $value );
					
			}

			foreach ($memberOrArray as $member => $value ) {
					
				if ( property_exists( $this , $member ) ) {

					$this -> { $member } = $value;

				}

			}

		}

